            <h2 class="hidden">Tipi di visite</h2>
            <p><div class="tipiVisita hidden"><b>Audiometria</b><br>
                L'audiometria e' un esame non invasivo che permette di valutare la capacita' uditiva di un individuo. Viene eseguita utilizzando un apparecchio chiamato audiometro, che emette suoni a diverse frequenze e intensita'. Durante l'esame, il paziente deve segnalare ogni volta che sente il suono emesso.<br></div>
              
              <div class="tipiVisita hidden"><b>Esame otorinolaringoiatrico per medicina dello sport</b><br>
              L'esame otorinolaringoiatrico per medicina dello sport e' un controllo medico che valuta la funzionalita' dell'apparato uditivo e respiratorio dell'atleta. Grazie a questa prestazione e' possibile individuare eventuali patologie o disfunzioni che potrebbero compromettere le prestazioni sportive.<br></div>

              <div class="tipiVisita hidden"><b>Diagnosi e Terapia dei Disturbi Respiratori del Sonno (Russamento ed Apnee)</b><br>
              La diagnosi e la terapia dei disturbi respiratori del sonno sono fondamentali per migliorare la qualita' della vita del paziente. I disturbi respiratori del sonno, come il russamento e le apnee, possono causare stanchezza, sonnolenza diurna e difficolta' di concentrazione. Grazie a questa prestazione e' possibile individuare la causa del disturbo e trovare la terapia piu' adatta.<br></div>

              <div class="tipiVisita hidden"><b>Endoscopia ORL</b><br>
              L'endoscopia ORL e' una procedura medica che permette di esaminare le vie respiratorie superiori, ovvero naso, gola e laringe, attraverso l'utilizzo di uno strumento chiamato endoscopio. Questa tecnica e' utilizzata per diagnosticare e valutare varie patologie, come ad esempio sinusiti, otiti, laringiti, polipi nasali e molto altro. L'esame e' semplice, indolore e non invasivo.<br></div>

              <div class="tipiVisita hidden"><b>Esame otofunzionale</b><br>
              L'esame otofunzionale e' un test che permette di valutare le funzioni dell'orecchio interno, come l'equilibrio e l'udito. Viene eseguito mediante l'utilizzo di stimolazioni sonore e termiche.<br></div>

              <div class="tipiVisita hidden"><b>Esame vestibolare</b><br>
              L'esame vestibolare e' una serie di test che mirano a valutare l'equilibrio e la coordinazione motoria del paziente. Viene utilizzato per identificare eventuali problemi dell'orecchio interno o del sistema nervoso centrale che possono causare vertigini, nausea o sbandamenti.<br></div>

              <div class="tipiVisita hidden"><b>Impedenzometria</b><br>
              L'impedenzometria e' un test audiologico non invasivo e indolore, che valuta la funzionalita' dell'orecchio medio — in particolare del timpano e della catena degli ossicini — misurando la loro risposta sonora e pressoria.<br></div>

              <div class="tipiVisita hidden"><b>Irrigazione orecchio</b><br>
              L'irrigazione dell'orecchio e' una procedura medica utilizzata per rimuovere il cerume in eccesso dall'orecchio esterno. La procedura viene eseguita da un operatore sanitario specializzato e prevede l'utilizzo di acqua tiepida per sciogliere il cerume, che viene poi rimosso con una leggera pressione dell'acqua.<br></div>

              <div class="tipiVisita hidden"><b>Laringoscopia</b><br>
              La laringoscopia e' un esame medico che permette di esaminare la laringe e le corde vocali. Si effettua tramite l'utilizzo di uno strumento chiamato laringoscopio che viene inserito nella bocca del paziente per osservare l'interno della laringe. L'esame e' indolore e dura solitamente pochi minuti.<br></div>

              <div class="tipiVisita hidden"><b>Medicazione otorinolaringoiatrica</b><br>
              La medicazione otorinolaringoiatrica e' una procedura medica che consiste nella pulizia e medicazione delle ferite o delle aree infette del naso, della gola o dell'orecchio. Questa prestazione viene eseguita da un medico otorinolaringoiatra e può essere eseguita sia in regime di day hospital che in regime di ricovero ospedaliero.<br></div>

              <div class="tipiVisita hidden"><b>Visita foniatrica</b><br>
              La visita foniatrica e' effettuata dal medico specialista in foniatria e prevede la valutazione delle abilita' del linguaggio orale e scritto, della parola, della voce, della deglutizione e delle funzioni orali anche con strumentazioni adeguate.<br></div>

              <div class="tipiVisita hidden"><b>Rinoscopia</b><br>
              Procedura diagnostica utilizzata in otorinolaringoiatria per esaminare l'interno delle cavita' nasali e delle strutture adiacenti per valutare eventuali problemi o anomalie nel naso o nelle vie aeree superiori.<br></div>

              <div class="tipiVisita hidden"><b>Visita audiologica</b><br>
              La visita audiologica e' un esame che permette di valutare la funzionalita' dell'orecchio e di individuare eventuali problemi uditivi. Durante la visita vengono eseguiti diversi test, come l'audiometria tonale e vocale, l'impedenzometria e la timpanometria.<br></div>

              <div class="tipiVisita hidden"><b>Visita ORL</b><br>
              La visita otorinolaringoiatrica, o visita ORL, e' una prestazione medica che permette di valutare la salute dell'orecchio, del naso e della gola. Durante la visita, il medico specialistico ORL effettua una valutazione completa delle strutture dell'area testa e collo, utilizzando strumenti specifici per l'ispezione e l'esame funzionale.<br></div>

              <div class="tipiVisita hidden"><b>Visita ORL pediatrica</b><br>
              La visita ORL pediatrica e' un esame clinico che viene eseguito dal medico specialista in otorinolaringoiatria su neonati, bambini e adolescenti. Durante la visita, il medico esegue una serie di esami per verificare la presenza di eventuali patologie dell'orecchio, del naso e della gola. L'esame e' indolore e non invasivo.<br></div>

              <div class="tipiVisita hidden"><b>Visita otochirurgica</b><br>
              La visita otochirurgica e' una prestazione medica che permette di analizzare lo stato di salute dell'orecchio e del canale uditivo. Viene effettuata da un medico specializzato in otorinolaringoiatria e può essere utile per individuare eventuali patologie dell'orecchio. La visita non e' dolorosa e dura circa 20-30 minuti.<br></div>

              <div class="tipiVisita hidden"><b>Lavaggio otorinolaringoiatrico</b><br>
              Il lavaggio otorinolaringoiatrico e' una procedura medica che consiste nel rimuovere il muco, le secrezioni e le croste presenti nelle cavita' nasali e nelle orecchie. Si tratta di un intervento semplice e sicuro che viene effettuato ambulatorialmente.<br></div>
